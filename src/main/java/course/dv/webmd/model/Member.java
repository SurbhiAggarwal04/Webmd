package course.dv.webmd.model;

public class Member {
	
	String memberName;
	int memberHelpfulVotes;
	String memberType;
	int memberFollowerNumber;
	
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public int getMemberHelpfulVotes() {
		return memberHelpfulVotes;
	}
	public void setMemberHelpfulVotes(int memberHelpfulVotes) {
		this.memberHelpfulVotes = memberHelpfulVotes;
	}
	public String getMemberType() {
		return memberType;
	}
	public void setMemberType(String memberType) {
		this.memberType = memberType;
	}
	public int getMemberFollowerNumber() {
		return memberFollowerNumber;
	}
	public void setMemberFollowerNumber(int memberFollowerNumber) {
		this.memberFollowerNumber = memberFollowerNumber;
	}
	
	
}
